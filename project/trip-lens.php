		<h1>Iterative prototyping</h1>
		<h2>User flows</h2>
		<p>
			To gather requirements that users have, we created a user flow based on our findings. The flows shows a single time line, which starts by adding the personal information. The flow then branches out when the user either joins or creates a trip. Finally, the user can explore the countries by moving the lens over the map.
		</p>
		
		<h2>Wireframing</h2>
		<p>
			We created wireframes for the three parts of the user interface: Setting up a user profile, creating a trip and adding users and exploring the map and handling the lens.
		</p>
		
		<h2>Paper prototyping</h2>
		<p>
			We created a paper prototype using transparent foil to simulate changing elements and highlighting parts of the map. We also realised that the metaphor of a lens could be expanded; as the lens itself constitutes an object itself, reifying a filter for the attached user information, we added a delete button to indicate that several lenses with different information could be created. Letting a user superpose lenses pushes the explorational nature of the concept even further.
		</p>
		
