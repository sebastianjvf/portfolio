<section id="project-two" class="content">
	<div class="inner">
		<h1>Ideation</h1>
		<h2>Task</h2>
		<p>
			The task was to develop a design concept which solves a user problem in the domain of travel and/or public transport. The goal of the class was to go through the user-centred design cycle rapidly and deliver a final video prototype at the end of one week. As part of the ideation phase, group members went to observe users and conduct interviews. My interviews were conducted in a group travelling abroad.
		</p>
		
		<h2>Interviews and analysis</h2>
		<p>
			The interviews conducted were quite informal; rather than guiding the conversation, I first observed how users were currently solving their problem and what tools they were using. Thereafter, I asked questions about what they thought caused problems during the planning phase.
		</p>
		
		<p>
			From my observations and interviews I could see that every participant in the travel group had their own tools for dealing with problems. Participants seemed to disagree about the tools, mentioning what made them the perfect fit; the problem was that other members were unfamiliar with the tools someone else usually used. Some participants were not sure about which countries they could go to, some did not like to go to certain countries which had been mentioned. A table on a paper was created for each country marking if the person would like to go there and would be able to go, according to the passport. As a last step, a common time frame had to be fun. After a small discussion, a Doodle was used with 90 days.
		</p>
		
		<p>
			Two other members turned out to have interviewed people planning trips abroad as well; we had thus collected useful insight and decided to focus our design concept on group travelling.
		</p>
		
		<h2>Personas</h2>
		<p>
			As a first step, we analysed the participants from the group travels we had interviewed. As a result, we set up three personas, bundling intentions and problems according to the insights we had gathered:
		</p>
		
		<ol>
			<li>Ben, the traveller without time</li>
			<li>Nicole, the traveller with a restrictive passport</li>
			<li>Damien, the picky traveller</li>
		</ol>
		
		<h2>Scenario</h2>
		<p>
			A scenario was written down, in which we modelled a common group situation; later on, we would rewrite the same scene into a story board to when users used our design concept for the video prototype. The main parts of the scene played out the three problems we had identified: time, place and passport restrictions.
		</p>
		
		<h2>Design brief</h2>
		<p>
			The design concept of trip lens is centred around the lens to which users can attach their personal information. Moving the lens over a map will show if users, according to their personal information, can and want to go to a certain country by colouring the country accordingly.
		</p>
	</div>
</section>

